#  2. INTEGER k
#

def maximumToys(prices, k):
    prices = sorted(prices)
    count_toy = 0
    cum_toy_price = 0
    for price in prices:
        cum_toy_price += price
        if cum_toy_price <= k:
            count_toy += 1
            
    return count_toy
            

